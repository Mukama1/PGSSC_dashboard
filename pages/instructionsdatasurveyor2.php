<p>For the qualitative portion of this study, we will be using semi-structured interviews to understand the factors related to the provision of safe, timely, affordable surgical care.</p>
<p>The goal of these structured interviews is for you to guide a conversation between you and the interviewee in order to get as much information that will be helpful to this study as possible. Above all, these interviews should be conversational and an opportunity for you to gather as much information as you can from the person you are interviewing. To that end, try to let the interviewee talk as much as possible. It is not necessary to ask the provided questions—they are simply there to a) give you an idea of the information we would like to have so you can guide the conversation to obtain that information, and b) give you specific questions to ask if the conversation is slow.</p>
<p>For each interview subject there is a framing statement that you can use to explain to the interviewee what type of information you are hoping to learn. This qualitative tool investigates the surgical system in 5 different sectors: Infrastructure, Workforce, Service Delivery, Financing, and Information Management. Within each sector, there are bolded subtopic questions of interest. For example, subtopics of interest under Infrastructure include "Describe your facility's infrastructure and how it affects your facility's ability to provide surgical care" and "What are the key challenges your facility faces in terms of infrastructure?". Under each bolded subtopic, there are <i>italicized specific questions</i> of interest. For example, under the first subtopic within Infrastructure, the first specific question of interest is <i>"How frequently does your facility experience interruptions in basic utilities (e.g running water, electricity)? How do you work around these interruptions?"</i></p>

<h5><B>Who to interview</B></h5>
<p>The Qualitative Hospital Interview Tool has four sets of interviews, one for each of type of hospital employee you will interview: 
1. Hospital director/CEO, or other management personnel. This interview is meant to gain an understanding of the managerial perspective (rather than clinical). 
2. Surgeon or obstetrician/gynecologist
3. Anesthesia provider
4. Surgical ward nurse. Please interview the highest-ranked person you can find in each of these four categories. However, we understand that it can sometimes be difficult to find interviewees, so don't worry if you cannot find someone to interview in each category. 
Note that if an interview subject serves in dual capacities, questionnaires can be adapted as necessary.  </p>

<h5><B>How to use this qualitative tool</B></h5>
<p>Step 1: Introduce the interviewee to our Framing statement: Ex. The purpose of this interview is to understand the key factors affecting the provision of safe, affordable and timely surgical care – from a management perspective – at this facility. We would like to understand the challenges you face as well as the main areas for improvement at this facility? 
Listen to the interviewee's response. If s/he has a lot to say, you should let him/her keep talking, steadily guiding him or her through the sectors of interest. If more is needed to get the conversation started, or if it slows at any point, proceed to Step 2. </p>

<P>Step 2: Ask the bolded subtopic questions within each factor of interest. For example, the first question under Workforce in the hospital director interview is "Describe your facility's human resources and how they affect your ability to provide surgical care". To keep the conversation flowing, you can continue with these bolded questions as long as is necessary to obtain the desired data. 
If further help is needed, proceed to Step 3: </P>

<P>Step 3: The italicized specific questions underneath each bolded one can serve as specific prompts to ensure that all of the necessary information is collected. It is not necessary to ask each italicized question, but they are available for use at your discretion. 
</p>

<h5><B>Data recording and analysis</B></h5>
<p>It will be best to audio record the interviews as you are carrying them out. This can be done on your cell phone or with an external recorder. You should ask permission prior to recording the interviewee. You should take field-notes on the interview responses as they are responding. Please transcribe all interviews for a particular hospital within two weeks of the hospital visit. Save both the audiofile of the interview and the transcription on a Dropbox folder labeled with the location of the hospital.</p> 

<P>A more detailed analytic tool will be presented as the project progresses. In the interim, the researchers should familiarize themselves with the Framework method of analysis[1 2]. Data analysis will be conducted using the interview transcriptions. Within two weeks of each hospital visit, one reviewer should read the transcription from each interview at least three times. While reading, the reviewer should "code" the interview transcript by annotating notes, identified themes, or observations on the interview content. Please upload the coded review of each transcript to Dropbox as well. 
</p>

<h5><B>Sample script</B></h5>
<p>The sample script below may aid in starting off the qualitative component of the interview:
"Hi [name of interview subject], we are conducting a research project to try and understand surgical care in the state of Amazonas. At each facility we are visiting, we are interviewing people to understand the factors that affect their ability to provide safe, timely surgical care, what barriers exist, and how those barriers might be overcome.  Before I ask specific questions, do you have any initial thoughts you would like to offer?"
</p>



