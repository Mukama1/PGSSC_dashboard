<p>Please answer every question on the assessment, paying attention to the prompts for the type of answer requested. For example, when a number sign is listed in the answer box, please enter in a number.</p>
<p>For questions with multiple choice answers, please select only one answer. When filling out the walk-through tool, you may be tempted to write in an answer that is not one of the answer choices. Although supplementary information can be helpful, you are encouraged to try to limit yourself to the answer choices provided, as written-in answers are difficult to analyze in quantitative terms and harm standardization. Notes should be added to the margins of the document or included in the qualitative interview so they can inform the broader analysis. </p>
<p>Some questions will ask for a specific number, such as the number of radiologists employed by the facility. For these questions, record a single number to the best of your ability. If it is not possible to record a single number, please record a range of numbers, as appropriate.</p>

<h4><B>Guide to specific questions</B></h4>

<B>General Information</B> 
<p>Qualification of hospital director: Please list the academic degrees of the medical director. </p>

<B>Hospital Walk Through->Operating Room</B>
<p>How many functional operating rooms are there? 
Please list the number of rooms in the hospital in which an operation could be performed. Note that these rooms do not necessarily need to be in regular use, but they must be functional at the time of visit. Similarly, questions asking how many functional anesthesia machines or functional ventilators are available should be answered as the number that are functional at the time of the visit. In addition, for any question regarding availability, please consider that as availability when needed.</p>

<B>Hospital Walk Through->Operating Room</B>
<p>If patients are required to purchase or bring any of the listed supplied, this should not be counted as being available in the hospital.</p>

<B>Hospital Walk Through->Post Anesthesia Care Unit</B>
<p>How many nurses are dedicated to the PACU? 
Please list the average number of nurses who are working in the PACU at any given time. Include only nurses who are working in the PACU during the entirety of that shift. For example, nurses who spend time during one shift in both a general ward and the PACU would not count as nurses dedicated to the PACU. </p>

<B>Hospital Walk Through->Blood Supply</B>
<p>Were blood bank services available in the past month?
Please check yes blood was available consistently within the past month. Check "No" if blood was not consistently available.</p>

<B>Hospital Walk Through->Radiology</B> 
<p>Availability of 24-hour radiology services refers to the ability to obtain and have an image interpreted a 24 hours a day.</p>

<B>Infrastructure->Access and referral systems</B> 
<p>For the first question, "What is the population in the catchment are of your hospital?" the term "catchment area" refers to the population in the geographical area the hospital is supposed to serve. For example, if this is a district hospital, the population of the district in which the hospital is located is the catchment population for question one.</p> 

<P>The second question, "What is the population of your true catchment area?" is asking for the size of the true population the hospital serves. For example, if a district hospital in reality serves a larger population, the "catchment population" for question two would be the size of the true population that comprises the patient base.</P>

<B>Infrastructure->Procedures </B> 
<p>For this section, please try to find the exact number of specific procedures that were conducted in the past month. 
To obtain this data, please follow this hierarchy of data sources:
1. Surgical department records
2. If surgical department records do not exist for every operation, please refer to operative logbooks. 
3. If operative logbooks do not exist or are incomplete, supplement by data from electronic medical records. 
4. If there is no electronic medical record or data are still incomplete, supplement with data from paper charts 
If an exact number cannot be obtained, please obtain a best estimate from the Chair of the Surgical Department or other faculty member. </p>

<B>Workforce</B> 
<p>Questions related to availability of staff do not necessarily mean in-hospital availability, but rather availability in a timely manner for emergencies.</p>

<B>Service Delivery->Surgical volume</B> 
<p>This section asks you for the monthly average number of specific procedure categories performed over the past six months. As in the Infrastructure->Procedures section, use the same hierarchy of data sources to find operative volume numbers per month, and average those numbers.  If exact numbers cannot be obtained, please record a best estimate or range from the Chair of the Surgical Department or other clinician. </p>

<B>Financing->Budget allocation</B> 
<p> Total annual hospital budget: 
Please record the total hospital budget, accounting for all funding sources (i.e. not just the ministry of health). </p>

<B>Financing->Cost section</B> 
<p>To obtain this data, please follow this hierarchy of data sources:
Tier 1: Actual patient charges from the billing register
Tier 2: Best estimate from chair of surgical department or other clinician. </p>
<h4><B>Data entry</B></h4>
<p>Please enter the data in the Quantitative Data excel spreadsheet on Dropbox within one week of each hospital visit. Each larger section of the tool is color-coded. Columns are validated such that you can only pick from the selected answer choices for the multiple-choice questions. Questions that ask for a specific number, such as the number of administrative staff employed by the facility or the number of C-sections performed last month, will restrict your answer to a single number. For these questions, record a single number to the best of your ability. For questions in which a range of numbers may be a more appropriate answers, you will be allowed to enter in the range. On the excel sheet, this option is available for questions that ask for an average number, patient population data, nurse:patient ratio, hospital budget, and out-of-pocket cost to patients, as a range may be a more appropriate answer to these questions. However, if you have an exact number for these questions, you may enter that as well. </p>
	
<p>There are be additional free text boxes in which you can enter notes at the end of each section. If you are unsure how best to enter a data point, please highlight that box in the excel spreadsheet and contact us. </p>


